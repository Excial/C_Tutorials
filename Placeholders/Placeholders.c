#include <stdio.h>

int main()  {

    float a = 1.7309;
    double b = 2.35643;
    int c = 5;
    char d = 'a';

    printf("Float = %f", a); //%f format specifier to print float types
    printf("\n");
    printf("Double = %lf", b); //we use %lf to print double values.
    printf("\n");
    printf("Int = %d", c); // to print int, we use %d int specifier
    printf("\n");
    printf("Char = %c", d); // To print char, we use %c format specifier.
    printf("\n");
    return 1;

}
